# 便捷函数
# ============================================================

def get_stock_pool(pool_type: str = '创业板龙头') -> list:
    """
    获取指定类型的股票池

    Args:
        pool_type: 股票池类型
            - '创业板龙头': 约100只创业板龙头
            - '深市主板龙头': 约50只深市主板龙头
            - '沪市主板龙头': 约50只沪市主板龙头
            - '全A股龙头': 约200只主板+创业板龙头
            - '全A股': 动态获取约4000只全A股（排除科创板/北交所）

    Returns:
        list: 股票代码列表
    """
    if pool_type == '创业板龙头':
        return 创业板龙头池
    elif pool_type == '深市主板龙头':
        return 深市主板龙头池
    elif pool_type == '沪市主板龙头':
        return 沪市主板龙头池
    elif pool_type == '全A股龙头':
        return 全A股龙头池
    elif pool_type == '全A股':
        return get_full_a_stock_pool()
    else:
        print(f"未知的股票池类型: {pool_type}")
        return 创业板龙头池


