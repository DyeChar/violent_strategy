# 动态股票池（从API获取，排除科创板和北交所）
# ============================================================

def get_full_a_stock_pool(use_cache: bool = True) -> list:
    """
    获取全A股股票池（排除科创板和北交所）

    范围：
    - 沪市主板：600/601/603/605开头
    - 深市主板：000/001/002/003开头
    - 创业板：300开头
    排除：
    - 科创板：688开头
    - 北交所：8开头、4开头

    Args:
        use_cache: 是否使用本地缓存（缓存有效期30天）

    Returns:
        list: 股票代码列表
    """
    import os
    import json
    import baostock as bs
    from datetime import datetime

    cache_file = os.path.join(os.path.dirname(__file__), 'output', 'full_stock_pool.json')

    # 检查缓存
    if use_cache and os.path.exists(cache_file):
        try:
            cache_data = json.load(open(cache_file, 'r'))
            cache_date = cache_data.get('date', '')
            cache_dt = datetime.strptime(cache_date, '%Y%m%d')
            if (datetime.now() - cache_dt).days < 30:  # 缓存30天有效
                print(f"使用缓存的股票池（{len(cache_data['codes'])}只）")
                return cache_data['codes']
        except Exception:
            pass

    print("从baostock获取全A股股票列表...")

    # 登录baostock
    lg = bs.login()
    if lg.error_code != '0':
        print(f"baostock登录失败: {lg.error_msg}")
        return 全A股龙头池  # 返回预定义池

    try:
        # 获取证券信息
        rs = bs.query_all_stock(day=datetime.now().strftime('%Y-%m-%d'))

        if rs.error_code != '0':
            print(f"获取股票列表失败: {rs.error_msg}")
            return 全A股龙头池

        stocks = []
        while rs.next():
            row = rs.get_row_data()
            code = row[0]  # 格式如 sh.600000

            # 提取股票代码
            if '.' in code:
                code = code.split('.')[1]

            # 过滤条件：排除科创板和北交所
            # 保留：600/601/603/605/000/001/002/003/300
            # 排除：688（科创板）、8开头、4开头（北交所）
            if code.startswith('688'):  # 科创板
                continue
            if code.startswith('8') or code.startswith('4'):  # 北交所
                continue

            # 保留主板和创业板
            valid_prefixes = ['600', '601', '603', '605', '000', '001', '002', '003', '300']
            if any(code.startswith(prefix) for prefix in valid_prefixes):
                stocks.append(code)

        bs.logout()

        print(f"获取到全A股股票 {len(stocks)} 只")

        # 保存缓存
        os.makedirs(os.path.dirname(cache_file), exist_ok=True)
        cache_data = {
            'date': datetime.now().strftime('%Y%m%d'),
            'codes': stocks
        }
        with open(cache_file, 'w') as f:
            json.dump(cache_data, f)

        return stocks

    except Exception as e:
        print(f"获取股票池异常: {e}")
        bs.logout()
        return 全A股龙头池


